public class StructuraFor {

	public static void main(String[] args) {
        
		System.out.println("Pisica mea face meow de 3 ori: ");
		for (int i = 0; i < 3; i++) { 
			System.out.println("meow");
		}
	}

}

// stuctura for initializeaza variabila i, apoi verifica daca acea conditie (i < 3) este adevărată, 
// dacă este adevărată va executa instructiunea din acolade, apoi va incrementa i-ul cu o unitate
// când i-ul a depășit valoarea 3 se va ieși din structură


// cu structura for se parcurg vectori, matrici și intervale
